/// 播放器枚举定义
/// 
/// 包含播放器模式、内容类型、控制栏类型等枚举

/// 播放器模式枚举
/// 
/// 定义播放器的显示模式，影响 UI 布局和交互方式
enum PlayerMode {
  /// 小窗模式 - 在详情页内嵌播放
  window,
  
  /// 全屏模式 - 占满整个屏幕
  fullscreen,
  
  /// 画中画模式 - 悬浮小窗播放
  pip,
  
  /// 流模式 - 短剧流竖屏滑动播放
  flow,
}

/// 内容类型枚举
/// 
/// 定义播放内容的类型，影响播放器配置和行为
enum ContentType {
  /// 短剧 - 单集短视频，通常 1-3 分钟
  shorts,
  
  /// 电视剧 - 多集长视频，每集 20-60 分钟
  tv,
  
  /// 电影 - 单集长视频，通常 90-180 分钟
  movie,
  
  /// 短剧流 - 随机短剧流，竖屏滑动播放
  shortsFlow,
}

/// 控制栏类型枚举
/// 
/// 定义播放器控制栏的显示级别
enum PlayerControlsType {
  /// 完整控制栏 - 包含所有功能按钮
  full,
  
  /// 基础控制栏 - 仅包含播放/暂停、进度条
  basic,
  
  /// 最小控制栏 - 仅包含播放/暂停
  minimal,
  
  /// 无控制栏 - 隐藏所有控制元素
  none,
}
